#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char *tab;

	tab = str;
	while (*tab != '\0')
	{
		if ('A' <= *tab && *tab <= 'Z')
		{
			*tab += 'a' - 'A';
		}
		tab++;
	}
	return (str);
}
